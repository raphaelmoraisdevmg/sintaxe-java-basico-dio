package com.br.dio.tiposevariaveis;

public class PriemeiraAula {

	public static void main(String[] args) {
		// prestar atenção na hora de escrever um número com o tipo double
		double salario = 2500.00; // correto 
		double outroValor = 2.500; // errado 
		long valorGrande = 25671112234656L;
		float pi = 3.14F;
		// terceira aula
		// toda constante devera ser escrita com final e o seu nome em caixa alta
		final int IDADE = 18;
		System.out.println(IDADE);
		byte numeroPequeno = (byte)10;
		System.out.println(numeroPequeno);
	}

}
